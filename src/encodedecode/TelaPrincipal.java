    private void btnDecryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecryptActionPerformed
        // TODO add your handling code here:
        if(txtInput.getText() == null || txtInput.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "The text input box can not be empty!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                final JTextArea txtArea = new JTextArea();

                String txtEncoded = txtInput.getText();
                txtArea.setText(txtEncoded);

                byte[] decodedBytes = Base64.getDecoder().decode(txtEncoded);
                String convertedText = new String(decodedBytes);
                txtOutput.setText(convertedText);
            }catch(IllegalArgumentException exception){
                JOptionPane.showMessageDialog(rootPane, "This hash is invalid.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDecryptActionPerformed

    private void btnCopyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCopyActionPerformed
        // TODO add your handling code here:
        if(txtOutput.getText() == null || txtOutput.getText().trim().equals("")){
            //JOptionPane.showMessageDialog(rootPane, "The text output box is empty!");
            JOptionPane.showMessageDialog(rootPane, "The text output box is empty!", "Error", JOptionPane.ERROR_MESSAGE); 
        }else{
            Clipboard board = Toolkit.getDefaultToolkit().getSystemClipboard();
            ClipboardOwner selection = new StringSelection(txtOutput.getText());
            board.setContents((Transferable) selection, selection);
            JOptionPane.showMessageDialog(rootPane, "Text copied.", "Success", WIDTH);
        }
    }//GEN-LAST:event_btnCopyActionPerformed

    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        // TODO add your handling code here:
        if(txtInput.getText() == null || txtInput.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "The text input box can not be empty!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            final JTextArea txtArea = new JTextArea();

            String simpleText = txtInput.getText();
            txtArea.setText(simpleText);

            String encoded = Base64.getEncoder().encodeToString(simpleText.getBytes());

            String convertedText = encoded;
            txtOutput.setText(convertedText);
        }
    }//GEN-LAST:event_btnEncryptActionPerformed

